function Apply-Translations($path, $map){
  $obj = Get-Content -Raw $path | ConvertFrom-Json
  foreach($k in $map.Keys){
    if($obj.PSObject.Properties.Name -contains $k){
      $obj.$k = $map[$k]
    }
  }
  ($obj | ConvertTo-Json -Depth 20) | Set-Content -Path $path -Encoding UTF8
}

