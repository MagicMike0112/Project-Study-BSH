function Add-Keys($path) {
  $obj = Get-Content -Raw -Path $path | ConvertFrom-Json
  foreach ($k in $new.Keys) {
    if (-not ($obj.PSObject.Properties.Name -contains $k)) {
      $obj | Add-Member -NotePropertyName $k -NotePropertyValue $new[$k]
    }
  }
  foreach ($k in $meta.Keys) {
    $metaKey = '@' + $k
    if (-not ($obj.PSObject.Properties.Name -contains $metaKey)) {
      $obj | Add-Member -NotePropertyName $metaKey -NotePropertyValue @{ placeholders = @{ ($meta[$k]) = @{} } }
    }
  }
  $json = $obj | ConvertTo-Json -Depth 20
  Set-Content -Path $path -Value $json -Encoding UTF8
}

